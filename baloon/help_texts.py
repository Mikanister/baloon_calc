HELP_FORMULAS = """
ФОРМУЛИ РОЗРАХУНКУ

1. Підйомна сила (закон Архімеда):
   F = (ρ_повітря - ρ_газу) × V × g
   де:
   - ρ_повітря - щільність повітря на висоті (кг/м³)
   - ρ_газу - щільність газу (кг/м³)
   - V - об'єм аеростата (м³)
   - g - прискорення вільного падіння (9.81 м/с²)

2. Щільність газу на висоті (ідеальний газовий закон):
   ρ = P / (R_specific × T)
   де:
   - P - тиск (Па)
   - R_specific - питома газова стала (Дж/(кг·К))
   - T - температура (К)

3. Атмосферна модель (ISA):
   T = T_sea - LAPSE_RATE × h
   P = P_sea × (T/T_sea)^(g/(R×LAPSE_RATE))
   де:
   - LAPSE_RATE = 0.0065 К/м
   - T_sea - температура на рівні моря (К)

4. Напруга в оболонці (для сфери):
   σ = ΔP × r / (2 × t)
   де:
   - ΔP - різниця тисків (Па)
   - r - радіус (м)
   - t - товщина оболонки (м)

5. Втрати газу через оболонку:
   Q = (permeability × S × ΔP × t) / thickness
   де:
   - permeability - коефіцієнт проникності (м²/(с·Па))
   - S - площа поверхні (м²)
   - ΔP - різниця тисків (Па)
   - t - час (с)
"""

